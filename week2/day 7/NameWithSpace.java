class NameWithSpace{
   public static void main(String[] args){
   System.out.println("Name with 8 space after every word");
   System.out.println("H\tA\tR\tD\tI\tK");

   System.out.println("Name with 8 space with next line ");
   System.out.println("H\n\tA\n\t\tR\n\t\t\tD\n\t\t\t\tI\n\t\t\t\t\tK");

   System.out.println("Name with single space after every character");
   System.out.println("H\sA\sR\sD\sI\sK");
}
}