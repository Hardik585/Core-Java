class NameInQuote{
  public static void main(String[] args){
    
       System.out.println("Name in Double Quote ");
       System.out.println("\"Hardik\"");
       System.out.println("Name in Single Quote");
       System.out.println("\'Hardik\'");
       System.out.println("We can use single quote inside the double quote ");
       System.out.println("'Hardik'");
    
  }

}